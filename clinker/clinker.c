/**************************************************************
*
*  clinker.c -- ORCA/C reimplementation of the ORCA/M linker
*
*  Accepts the same command-line interface as the assembly
*  linker.  Produces identical OMF v2 output suitable for
*  loading by GS/OS.
*
*  Usage:
*    clinker [flags] file ... [keep=outfile]
*
*  Flags (same as asm linker):
*    +L    list segment info
*    +S    list symbol table after link
*    -X    disable express-load segment (default: enabled)
*    +M    memory-only link (no keep file written)
*    -C    disable compact records
*    -P    disable progress messages (default: show progress)
*
*  Shell variable:
*    gsplusSymbols=1   inject WDM prologue + write .symbols file
*
**************************************************************/

#pragma keep "clinker"
#pragma optimize 9

#include "clinker.h"

/* ----------------------------------------------------------
   Global state
   ---------------------------------------------------------- */

BOOLEAN  opt_list     = FALSE;
BOOLEAN  opt_symbols  = FALSE;
BOOLEAN  opt_express  = TRUE;  /* ExpressLoad on by default (matches iix link); -X disables */
BOOLEAN  opt_memory   = FALSE;
BOOLEAN  opt_compact  = TRUE;
BOOLEAN  opt_progress = TRUE;
BOOLEAN  opt_gsplus   = FALSE;
char    *keepName = NULL;   /* malloc'd in main() */
char    *baseName = NULL;

int      numErrors  = 0;
InputFile *inputFiles = NULL;
InputFile *inputTail  = NULL;
LibFile   *libFiles   = NULL;
OutSeg    *outSegs    = NULL;
int        numOutSegs = 0;
dword      sfSig      = 0;
int        lastDataNumber = 0;  /* link-wide data-area counter */

/* ----------------------------------------------------------
   Error reporting
   ---------------------------------------------------------- */

void LinkError(const char *msg, const char *name)
{
numErrors++;
if (name && name[0])
    fprintf(stderr, "clinker: error: %s: %s\n", msg, name);
else
    fprintf(stderr, "clinker: error: %s\n", msg);
}

void FatalError(const char *msg)
{
fprintf(stderr, "clinker: fatal: %s\n", msg);
exit(1);
}

/* ----------------------------------------------------------
   Output segment management
   ---------------------------------------------------------- */

OutSeg *FindOrCreateOutSeg(const char *loadName, const char *segName,
                            word kind)
{
OutSeg *s;

/* Merge key is LOADNAME only.  Per GS/OS Reference Appendix F
 * LOADNAME is "the name of the load segment that will contain the
 * code generated by the linker for this segment; more than one
 * segment in an object file can be merged by the linker into a
 * single segment in the load file."  The spec further states that
 * when merged segments have different KINDs, "the segment KIND of
 * the first object segment determines the segment KIND of the
 * entire load segment" — so we match on loadName alone, keep the
 * first-seen KIND, and do not require segType equality. */
for (s = outSegs; s; s = s->next) {
    if (strcmp(s->loadName, loadName) == 0) {
        /* Merge this segment's KIND flag bits into the output per the
         * stock ORCA/M linker rule (linker/out.asm:252-310, CheckHeader):
         *   bits $1D00 are ORed  — set if ANY contributing seg has them
         *   bits $E200 are ANDed — set only if ALL contributing segs do
         * The AND on $E200 is why PRIVATE ($4000) and DYNAMIC ($8000)
         * clear on output whenever any merged input lacked the bit.
         * Low byte (segType) stays as first-seen. */
        word orBits  = (word)(kind & 0x1D00);
        word andMask = (word)((kind & 0xE200) | 0x1DFF);
        s->kind = (word)((s->kind | orBits) & andMask);
        return s;
        }
    }

/* Create a new output segment */
s = (OutSeg *)malloc(sizeof(OutSeg));
if (!s) FatalError("out of memory (OutSeg)");
memset(s, 0, sizeof(OutSeg));

strncpy(s->loadName, loadName, NAME_MAX - 1);
strncpy(s->segName,  segName,  NAME_MAX - 1);
s->segType   = (word)(kind & 0x1F);
/* First contributor's KIND is taken verbatim, less the $0100 bankOrg
 * bit (stock CheckHeader at linker/out.asm:262 does `AND #$FEFF`).
 * Subsequent merges refine this per the OR/AND masks above. */
s->kind      = (word)(kind & ~0x0100);
s->banksize  = 0x10000L;
s->segNum    = ++numOutSegs;
s->length    = 0;

/* Append to list */
if (!outSegs) {
    outSegs = s;
    } else {
    OutSeg *t;
    for (t = outSegs; t->next; t = t->next) /* walk to end */ ;
    t->next = s;
    }

return s;
}

OutSeg *OutSegByNum(int n)
{
OutSeg *s;
for (s = outSegs; s; s = s->next)
    if (s->segNum == n) return s;
return NULL;
}

/* ----------------------------------------------------------
   Output data buffer helpers
   ---------------------------------------------------------- */

void GrowData(OutSeg *seg, long need)
{
long required = seg->dataLen + need + 16;
if (required <= seg->dataAlloc) return;
seg->data = (byte *)realloc(seg->data, (size_t)required);
if (!seg->data) FatalError("out of memory (GrowData)");
memset(seg->data + seg->dataAlloc, 0,
       (size_t)(required - seg->dataAlloc));
seg->dataAlloc = required;
}

void EmitData(OutSeg *seg, const byte *src, long len)
{
if (len <= 0) return;
GrowData(seg, len);
memcpy(seg->data + seg->dataLen, src, (size_t)len);
seg->dataLen += len;
}

void EmitZero(OutSeg *seg, long len)
{
if (len <= 0) return;
GrowData(seg, len);
memset(seg->data + seg->dataLen, 0, (size_t)len);
seg->dataLen += len;
}

/* ----------------------------------------------------------
   Relocation record management
   ---------------------------------------------------------- */

void AppendReloc(OutSeg *seg, long pc, byte patchLen, byte shift,
                 long value, int type, int segNum, int fileNum)
{
RelocRec *r = (RelocRec *)malloc(sizeof(RelocRec));
if (!r) FatalError("out of memory (RelocRec)");
r->pc       = pc;
r->patchLen = patchLen;
r->shift    = shift;
r->value    = value;
r->type     = type;
r->segNum   = segNum;
r->fileNum  = fileNum;
r->next     = NULL;

if (!seg->relocHead)
    seg->relocHead = r;
else
    seg->relocTail->next = r;
seg->relocTail = r;
}

